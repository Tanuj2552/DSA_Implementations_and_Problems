// return true if the given tree is a BST, else return false
void find_those(Node* root, vector<int> &v){
    if(root == NULL){return;}
    find_those(root->left,v);
    v.push_back(root->data);
    find_those(root->right,v);
}


class Solution
{
    public:
    bool isBST(Node* root) 
    {   
        vector<int> v;
        find_those(root, v);
        int flag = 0;
        for(int i = 0;i<v.size()-1;i++){
            if(v[i+1] <= v[i]){flag = 1; break;}
        }
        if(flag == 0){return true;}
        return false;
    }
};




